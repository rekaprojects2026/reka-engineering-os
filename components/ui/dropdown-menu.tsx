function DropdownMenuSubTrigger({
  className,
  inset,
  children,
  ...props
}: React.ComponentProps<typeof DropdownMenuPrimitive.SubTrigger> & { inset?: boolean }) {
  return (
    <DropdownMenuPrimitive.SubTrigger
      data-slot="dropdown-menu-sub-trigger"
      className={cn(
        "flex cursor-default select-none items-center gap-2 rounded-[calc(var(--radius-control)-2px)] px-2 py-1.5 text-[0.8125rem] outline-none focus:bg-[var(--color-surface-muted)] data-[state=open]:bg-[var(--color-surface-muted)]",
        inset && "pl-8",
        className
      )}
      {...props}
    >
      {children}
      <ChevronRight className="ml-auto h-4 w-4 opacity-50" />
    </DropdownMenuPrimitive.SubTrigger>
  )
}

